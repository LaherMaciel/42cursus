#include "push_swap.h"

t_stack	*ft_lstnew(int content)
{
	t_stack	*new_list;

	new_list = (t_stack *) malloc(sizeof(t_stack));
	if (!new_list)
		return (NULL);
	new_list->content = content;
	new_list->next = NULL;
	return (new_list);
}
