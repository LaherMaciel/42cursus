#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	char	*d;
	char	*s;
	size_t	i;

	d = (char *)dest;
	s = (char *)src;
	i = -1;
	if (!dest && !src)
		return (0);
	while (++i < n)
		d[i] = s[i];
	return (dest);
}
